<template>
  <div>
    <h1>A New component</h1>
    <p>There are currently {{ countLinks }} links</p>
    <button @click="removeAllLinks">Remove All</button>
    <p>{{ msg }}</p>
  </div>
</template>

<script>
import { mapGetters, mapMutations, mapActions } from "vuex";
export default {
  name: "statsWord",
  data() {
    return {
      msg: "",
    };
  },
  computed: {
    ...mapGetters(["countLinks"]),
  },
  methods: {
    ...mapMutations(["REMOVE_ALL"]),
    ...mapActions(["removeAll"]),
    removeAllLinks() {
      this.removeAll().then(() => {
        this.msg = "All have been Removed";
      });
    },
  },
};
</script>

<style scoped>
button {
  padding: 10px;
  margin-top: 30px;
  width: 100%;
  background: none;
  border: 1px solid lightgray;
  outline: 0;
  cursor: pointer;
}
</style>
